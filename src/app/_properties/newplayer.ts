export class NewPlayer {
  username: string;
  firstName: string;
  lastName: string;
  email: string;
  phoneNumber: string;
  password: string;
  constructor(username: string,
    firstName: string,
    lastName: string,
    email: string,
    phoneNumber: string,
    password: string) {
    this.username = username;
    this.firstName;
    this.lastName;
    this.email = email;
    this.phoneNumber = phoneNumber;
    this.password = password;
  }
}
